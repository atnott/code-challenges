#pragma once
#include "SudokuModel.hpp"
#include "SudokuView.hpp"
#include <string>

/**
 * @brief Класс Контроллера для игры Судоку
 * @details Выступает в роли посредника между Моделью и Представлением
 * Отвечает за обработку пользовательского ввода, управление фазами игры,
 * валидацию текстовых команд и обработку ошибочных ситуаций
 */
class SudokuController
{
    SudokuView view; ///< Компонент отображения
    SudokuModel model; ///< Компонент данных и бизнес-логики
    bool isRunning; ///< Флаг активности главного игрового цикла

    /**
     * @brief Парсинг строкового ввода игрока в числовые координаты и значение ячейки
     * @param[in] input исходная строка ввода
     * @param[out] row ссылка для записи вычисленного индекса строки (0-8)
     * @param[out] col ссылка для записи вычисленного индекса столбца (0-8)
     * @param[out] value ссылка для записи установленного значения (1-9)
     * @note Безопасно разбирает строку, преобразуя буквенные координаты 'A'-'I' и символы цифр
     */
    void parseInput(const std::string& input, int& row, int& col, int& value);
public:
    /**
     * @brief Конструктор по умолчанию, устанавливает флаг активности игры в true
     */
    SudokuController() : isRunning(true) {}

    /**
     * @brief Запуск главного управляющего игрового цикла
     * @details Выполняет последовательность действий:
     * 1. Инициализация игры (выбор уровня сложности пользователем)
     * 2. Генерация уникального игрового поля Моделью
     * 3. Запуск цикла обновления: очистка экрана, отрисовка через View, опрос ввода,
     * проверка условий победы/выхода/авторешения и обработка ошибок
     */
    void start();
};